const { PRODUCTS } = require('./dist/src/data/products/index.js');
const { INGREDIENTS } = require('./dist/src/data/ingredients.js');
const { CONFLICT_RULES } = require('./dist/src/data/compatibility.js');
const { QUESTIONS } = require('./dist/src/data/quiz.js');
const { en } = require('./dist/src/i18n/en.js');
const { ru } = require('./dist/src/i18n/locales/ru.js');
const { ru_content } = require('./dist/src/i18n/content/ru.js');
const fs = require('fs');
const path = require('path');

const OUT = path.join(__dirname, '..');

function t(key, lang) {
  const cat = lang === 'ru' ? ru : en;
  return cat[key] || en[key] || key;
}

// ---- products (both languages) ----
const products = PRODUCTS.map(p => {
  const rc = ru_content.products[p.id] || {};
  return {
    id: p.id,
    category: p.category,
    actives: p.actives,
    concerns: p.concerns,
    time: p.time,
    strength: p.strength,
    step: p.step,
    beginner: !!p.beginner,
    men: !!p.men,
    body: !!p.body,
    name: { en: p.nameEn, ru: rc.n || p.nameRu },
    blurb: { en: p.blurb, ru: rc.b || p.blurb },
    howTo: { en: p.howTo, ru: rc.h || p.howTo },
  };
});

// ---- ingredients (both languages) ----
const ingredients = INGREDIENTS.map(i => {
  const rc = ru_content.ingredients[i.key] || {};
  return {
    key: i.key,
    family: { en: i.family, ru: rc.family || i.family },
    name: { en: i.name, ru: rc.name || i.name },
    what: { en: i.what, ru: rc.what || i.what },
    benefits: { en: i.benefits, ru: (rc.benefits && rc.benefits.length ? rc.benefits : i.benefits) },
    caution: { en: i.caution, ru: rc.caution || i.caution },
    pairsWith: i.pairsWith,
    avoidWith: i.avoidWith,
  };
});

// ---- conflict rules (both languages) ----
const conflicts = CONFLICT_RULES.map(r => ({
  a: r.a, b: r.b, severity: r.severity,
  why: { en: r.why, ru: r.why },
  tip: { en: r.tip, ru: r.tip },
}));

// ---- quiz questions, resolved to real strings in both languages ----
const quiz = QUESTIONS.map(q => ({
  key: q.key,
  multi: !!q.multi,
  max: q.max || null,
  title: { en: t(q.titleKey, 'en'), ru: t(q.titleKey, 'ru') },
  sub: { en: t(q.subKey, 'en'), ru: t(q.subKey, 'ru') },
  options: q.options.map(o => ({
    value: o.value,
    label: { en: t(o.labelKey, 'en'), ru: t(o.labelKey, 'ru') },
    sub: { en: t(o.subKey, 'en'), ru: t(o.subKey, 'ru') },
  })),
}));

function writeGlobal(filename, varName, value) {
  const js = `/* AUTO-GENERATED by data/_build/export.js — do not edit by hand. */\nwindow.${varName} = ${JSON.stringify(value)};\n`;
  fs.writeFileSync(path.join(OUT, filename), js);
}

writeGlobal('products.js', 'PRODUCTS', products);
writeGlobal('ingredients.js', 'INGREDIENTS', ingredients);
writeGlobal('conflicts.js', 'CONFLICTS', conflicts);
writeGlobal('quiz.js', 'QUIZ', quiz);

console.log('products:', products.length, 'ingredients:', ingredients.length,
            'conflicts:', conflicts.length, 'quiz questions:', quiz.length);
